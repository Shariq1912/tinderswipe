  int _currentIndex = 0;

  void _handleSwipe(Direction direction) {
    setState(() {
      if (direction == Direction.left) {
        // Perform actions when swiped left
        print('Swiped Left');
        _currentIndex++;
      } else if (direction == Direction.right) {
        // Perform actions when swiped right
        print('Swiped Right');
        _currentIndex++;
      } else if (direction == Direction.up) {
        // Perform actions when swiped up
        print('Swiped Up');
        _currentIndex++;
      }
      else if (direction == Direction.down) {
        // Perform actions when swiped up
        print('Swiped Down');
        _currentIndex;
      }
    });
  }

